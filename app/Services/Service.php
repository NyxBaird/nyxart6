use Illuminate\Database\Eloquent\Model;

/**
 * Class Service
 * @package Services
 */
class Service
{
    /**
     * @var Model
     */
    protected $model;

    /**
     * Service constructor.
     * @param Model $model
     */
    public function __construct(Model $model)
    {
        $this->model = $model;
    }

    /**
     * @return Model
     */
    public function model()
    {
        return $this->model;
    }

    /**
     * @return \Illuminate\Database\Eloquent\Collection|static[]
     */
    public function all()
    {
        return $this->model->all();
    }
}